# tuple
my_tuple = ("apple","banana","cherry")
print(my_tuple)

# Tuples allow duplicate values:
my_tuple = ("apple","banana","cherry","apple","cherry")
print(my_tuple)

# Print the number of items in the tuple:
my_tuple = ("apple","banana","cherry")
print(len(my_tuple))

# One item tuple, remember the comma:
my_tuple = ("apple",)
print(my_tuple)

# not a tuple
my_tuple = ("apple")
print(type(my_tuple))

# String, int and boolean data types:
my_tuple1 = ("apple","banana","cherry")
my_tuple2 = (1,2,3,4,5,6)
my_tuple3 = (True,False,False)
print(my_tuple1)
print(my_tuple2)
print(my_tuple3)