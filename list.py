# list
print()
my_list = [1,2,3,4,5]
print(my_list)

# append method
my_list.append(7)
print(my_list)

# Changing the value of a specific element through its index :
my_list[5] = 6
print(my_list)

# extend method
my_list.extend([6,7,8])
print(my_list)

# remove method
my_list.remove(8)
print(my_list)

# insert method
my_list.insert(8,9)
print(my_list)