# sets
my_set = {1,2,3,4,5,6}
print(my_set)

# add method
my_set.add(7)
print(my_set)

# update method
my_set.update([8,9,10])
print(my_set)

# remove method
my_set.remove(10)
print(my_set)

# Duplicate values will be ignored:
my_set = {1,2,3,4,5,6,7,7,8,9,10}
print(my_set)

